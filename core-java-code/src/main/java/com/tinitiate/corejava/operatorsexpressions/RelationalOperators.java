package com.tinitiate.corejava.operatorsexpressions;

public class RelationalOperators {
    public static void main(String[] args) {
        int x = 10, y = 20;

        System.out.println("x > y: " + (x > y));    // false
        System.out.println("x < y: " + (x < y));    // true
        System.out.println("x == y: " + (x == y));  // false
        System.out.println("x != y: " + (x != y));  // true
        System.out.println("x >= y: " + (x >= y));  // false
        System.out.println("x <= y: " + (x <= y));  // true
    }
}
