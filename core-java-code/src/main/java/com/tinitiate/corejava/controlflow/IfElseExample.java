package com.tinitiate.corejava.controlflow;

public class IfElseExample {
    public static void main(String[] args) {
        int number = 5;

        if (number % 2 == 0) {
            System.out.println("Even number");
        } else {
            System.out.println("Odd number");
        }
    }
}

// Output:
// Odd number
