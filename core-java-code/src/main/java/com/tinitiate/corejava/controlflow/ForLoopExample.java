package com.tinitiate.corejava.controlflow;

public class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
    }
}

// Output:
// 1
// 2
// 3
// 4
// 5
