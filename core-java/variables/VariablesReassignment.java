// File: VariablesReassignment.java
public class VariablesReassignment {
    public static void main(String[] args) {
        int attempts = 1;
        System.out.println("attempts (initial) : " + attempts);
        attempts = 2; // reassigned
        System.out.println("attempts (updated) : " + attempts);
    }
}
/*
OUTPUT:
attempts (initial) : 1
attempts (updated) : 2
*/
