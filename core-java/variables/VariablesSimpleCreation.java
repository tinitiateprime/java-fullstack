// Simple Creation (Java)
public class VariablesSimpleCreation  {
    public static void main(String[] args) {
        int variable1 = 1;            // Integer (primitive)
        String variable2 = "test";    // String (reference type)
        double variable3 = 100.4;     // double (floating-point)

        // Print all three variables
        System.out.println("variable1 : " + variable1);
        System.out.println("variable2 : " + variable2);
        System.out.println("variable3 : " + variable3);
    }
}

/*
OUTPUT:
variable1 : 1
variable2 : test
variable3 : 100.4
*/